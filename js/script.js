$(document).ready(function(){
	/* For Filter Function */
	$("#filter button").click(function(){
		$("#filter .content").slideToggle().toggleClass('opened');
		var isVisible = $("#filter .content").is(".opened");
		if(isVisible === true){
			$("#filter button").css("transform", "rotate(180deg)");
		}
		else $("#filter button").css("transform", "rotate(0deg)");
	});
	
	var fprice = '.product-item';
	var fcolor = ' ';
	
	$("#filter input[type='radio']").change(function(){		
		var a = $(this).val();
		if (a == 0) fprice = '.product-item';
		else if(a == 1) fprice = '.priceA';
		else if(a == 2) fprice = '.priceB';
		else if(a == 3) fprice = '.priceC';
		else if(a == 4) fprice = '.priceD';
		else if(a == 5) fprice = '.priceE';
		else if(a == 6) fprice = '.priceF';
		showFilter(fprice, fcolor);
	});
	
	$("#filter input[type='checkbox']").change(function(){
		fcolor = " ";
		$("#filter input[type='checkbox']").filter(':checked').each(function(index){
			if(index == 0) fcolor = "."+$(this).val();
			else fcolor += ", ."+$(this).val();
		});
		showFilter(fprice, fcolor);
	});
});

/*For Filter Function*/
function showFilter(p, c)
{
	$('.product-item').hide();
	if( c == " ") $(p).show();
	else $(p).filter(c).show();
}

function addDiscount(items)
{
	$("div#wrapper").find('.product-item').each(function(index){
		if(items[index].sale == true){
			$(this).find('.product-details').append('<div class="discount">'+
			'<img src="images/discount-ribbon.png"></div>');
			$(this).find('.price').after('&nbsp<span>P</span><span>'
			+(items[index].price/2).toFixed(2)+'</span>');
		}
	});	
}
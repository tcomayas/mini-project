function addToCart(id, imgloc, price, sale)
{
	var cart_obj = new Array();
	var quantity = document.getElementById("q-"+id).value; 
	
	var size = document.getElementById("s-"+id).value;

	if(!(quantity <= 0))
	{
		var cont_flag = true;
		
		if(typeof(Storage) != "undefined"){
			if(!(sessionStorage.getItem("cart") === null))
			{
				cart_obj = JSON.parse(sessionStorage.cart);
				for(a = 0; a < cart_obj.length; a++)
				{
					if((cart_obj[a].id == id) && (cart_obj[a].size == size))
					{
						alert("You already have that item in the cart.");
						cont_flag = false;
						break;
					}
				}
			}
			
			if(cont_flag)
			{
				if(sessionStorage.cartCount)
					sessionStorage.cartCount = Number(sessionStorage.cartCount) + 1;
				else sessionStorage.cartCount = 1;
				
				cart_obj[Number(sessionStorage.cartCount) - 1] = cart(id, imgloc, price, quantity, size, sale);
				alert("Added to cart.");
				sessionStorage.cart = JSON.stringify(cart_obj);
			}
			
		}	
	}
	else alert("Cannot add item to cart: Minimum Quantity not met.");
}

function cart(cartId, cartImgLoc, cartPrice, cartQty, cartSize, cartSale)
{
	var cart = {
			       id: cartId,
				   imgLoc: cartImgLoc, 
	               price: cartPrice, qty: cartQty, 
			       size: cartSize, 
			       sale: cartSale
			   };
	return cart;
}

